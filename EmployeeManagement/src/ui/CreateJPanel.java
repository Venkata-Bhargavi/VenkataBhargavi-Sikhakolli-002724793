    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        if(checkError != null){
               JOptionPane.showMessageDialog(this,"Please provide valid inputs to all fields"); 

        }
        else{
        String name = txtEmployeeName.getText();
        String employeeId = txtEmployeeId.getText();
        int age = Integer.parseInt(txtAge.getText());
        String gender = genderRadioButton;
        long phone = Long.parseLong(txtPhone.getText());
        String email = txtEmail.getText();
        String positionTitle = txtPositionTitle.getText();
        String level = txtLevel.getText();
        Date startDate = jdcCalendar.getDate();
        String teamInfo = txtTeamInfo.getText();
        String photo = imagePath;
        
        EmployeeProfile ep = profileHistory.addNewProfile();
        
        // sets value to ArrayList from the fields that the data is entered.
        ep.setName(name);
        ep.setEmployeeId(employeeId);
        ep.setAge(age);
        ep.setPhone(phone);
        ep.setGender(gender);
        ep.setEmail(email);
        ep.setPositionTitle(positionTitle);
        ep.setStartDate(startDate);
        ep.setTeamInfo(teamInfo);
        ep.setLevel(level);
        ep.setPhoto(photo);
        
        // shows a dialogue message when the data clicked save button
        JOptionPane.showMessageDialog(this,"Employee details added"); 
        
        // empties all the fields after saving the entered data to ArrayList to facilitate new entries.
        txtEmployeeName.setText("");
        txtEmployeeId.setText("");
        txtAge.setText("");
        genderRadioButton = null; // making global variable null
        // unchecking radiobuttons by setting male and female to false
        rbtnMale.setSelected(false); 
        rbtnFemale.setSelected(false);
        txtEmail.setText("");
        txtPositionTitle.setText("");
        txtLevel.setText("");
        jdcCalendar.setDate(null);
        txtTeamInfo.setText("");
        txtPhone.setText("");
        lblPhoto.setText(null);
        }


    }//GEN-LAST:event_btnSaveActionPerformed

